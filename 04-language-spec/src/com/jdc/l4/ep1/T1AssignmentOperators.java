package com.jdc.l4.ep1;

public class T1AssignmentOperators {
	
	public static void main(String[] args) {
		String str = "Hello Java";
		System.out.println(str);
		
		Integer var = 10;
		System.out.println(var);
		
		var += 1;
		System.out.println(var);
		
		var -= 10;
		System.out.println(var);
		
		var *= 10;
		System.out.println(var);
		
		var /= 10;
		System.out.println(var);
		
		var %= 5;
		System.out.println(var);
	}

}
